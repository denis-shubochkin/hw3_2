import order from '../basic';

test('ordered massives not equaled with toBe', () => {
  const p = [
    { name: 'мечник', health: 10 },
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
  ];
  const exp = [
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
    { name: 'мечник', health: 10 },
  ];
  const result = order(p);
  expect(result).not.toBe(exp);
});

test('ordered massives equaled with toEqual', () => {
  const p = [
    { name: 'мечник', health: 10 },
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
  ];
  const exp = [
    { name: 'маг', health: 100 },
    { name: 'лучник', health: 80 },
    { name: 'мечник', health: 10 },
  ];
  const result = order(p);
  expect(result).toEqual(exp);
});
